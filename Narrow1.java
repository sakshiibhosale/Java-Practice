//primitive type casting(Narrowing)

package TypeCasting;

public class Narrow1
{
  public static void main (String [] args)
  {
	  int a = (int)23.23;
	  System.out.println(a);
	  
	  char ch=(char)150l;
	  System.out.println(ch);
	  
	  float f=(float)23.56563456345345;
	  System.out.println(f);
  }
}
