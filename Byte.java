//primitive TYpe Casting(Widening)

package TypeCasting;

public class Byte
{
  public static void main (String [] args)
  {
	  byte a=127;
	  short s=a;
	  int i=a;
	  long l=a;
	  float f=a;
	  double d=a;
	  
	  System.out.println(a);
	  System.out.println(s);
	  System.out.println(i);
	  System.out.println(l);
	  System.out.println(f);
	  System.out.println(d);
	  
  }
}
